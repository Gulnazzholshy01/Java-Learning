public class Loop {
    public static void main (String[] args) throws Exception {

        //DECLARATIONS 
        int x;
        int y = 5;

        //Initialize x
        x = 0; //Initialize control variable 
        while (x <= y) { //Variable tested or expession 
            System.out.println(x);
            x++; //Incrementing , alter control value
        }
    }
}
